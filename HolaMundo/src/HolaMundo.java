
public class HolaMundo {

	public static void main(String[] args) {
		// HolaMundo
		System.out.println("Hola Mundo!");
		// Declaración de Variables
		double m;
		double n;
		// Asignación de valor a variables
		m = 5.5;
		n = 3.5;

		// Impresión por pantalla
		System.out.println();
		System.out.println("La suma de m y n + 2 es igual a...");
		System.out.println();
		System.out.println("m=	" + (m + 2.0));
		System.out.println("m=	" + (n + 2.0));

		System.out.println("¡Hola Guzman!");

	}

}
